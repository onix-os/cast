//! Stateful Gluon adapter for generated source-lock declarations.

use std::{cmp::Ordering, fmt::Write as _};

use declarative_config::{
    DeclarationCodec, DeclarationEvaluationError, DeclarationEvaluator,
    Evaluation as DeclarationEvaluation, LanguageSpec, Limits, Source,
    SourceRoot,
};
use gluon_config::{EvaluationIdentity, GluonEngine};

use super::{ArchiveResolution, GitResolution, SourceLock, SourceResolution, ValidationError};

/// Marker which makes it explicit that a generated Gluon lock must not be
/// hand edited.
pub const GENERATED_GLUON_MARKER: &str = "// @generated by cast. DO NOT EDIT.\n";

const STANDALONE_GLUON_TYPES: &str = r#"type SourceResolution =
    | Archive {
        order : Int,
        url : String,
        sha256 : String,
    }
    | Git {
        order : Int,
        url : String,
        requested_ref : String,
        commit : String,
        materialization_sha256 : String,
    }

type SourceLock = {
    schema_version : Int,
    sources : Array SourceResolution,
}

"#;

#[derive(Debug, gluon_codegen::Getable, gluon_codegen::VmType)]
struct GluonSourceLock {
    schema_version: i64,
    sources: Vec<GluonSourceResolution>,
}

#[derive(Debug, gluon_codegen::Getable, gluon_codegen::VmType)]
enum GluonSourceResolution {
    Archive {
        order: i64,
        url: String,
        sha256: String,
    },
    Git {
        order: i64,
        url: String,
        requested_ref: String,
        commit: String,
        materialization_sha256: String,
    },
}

impl TryFrom<GluonSourceLock> for SourceLock {
    type Error = ValidationError;

    fn try_from(lock: GluonSourceLock) -> Result<Self, Self::Error> {
        let schema_version = u32::try_from(lock.schema_version).map_err(|_| ValidationError::IntegerOutOfRange {
            field: "schema_version".to_owned(),
            value: lock.schema_version,
            expected: "non-negative 32-bit integer",
        })?;
        let sources = lock
            .sources
            .into_iter()
            .enumerate()
            .map(|(index, source)| source.try_into_domain(index))
            .collect::<Result<_, _>>()?;
        Ok(Self {
            schema_version,
            sources,
        })
    }
}

impl GluonSourceResolution {
    fn try_into_domain(self, index: usize) -> Result<SourceResolution, ValidationError> {
        let convert_order = |order| {
            u32::try_from(order).map_err(|_| ValidationError::IntegerOutOfRange {
                field: format!("sources[{index}].order"),
                value: order,
                expected: "non-negative 32-bit integer",
            })
        };

        Ok(match self {
            Self::Archive { order, url, sha256 } => SourceResolution::Archive(ArchiveResolution {
                order: convert_order(order)?,
                url,
                sha256,
            }),
            Self::Git {
                order,
                url,
                requested_ref,
                commit,
                materialization_sha256,
            } => SourceResolution::Git(GitResolution {
                order: convert_order(order)?,
                url,
                requested_ref,
                commit,
                materialization_sha256,
            }),
        })
    }
}

/// Gluon source-lock codec with its language and evaluator policy fixed at
/// construction.
#[derive(Debug, Clone)]
pub struct GluonSourceLockCodec {
    engine: GluonEngine,
}

impl Default for GluonSourceLockCodec {
    fn default() -> Self {
        Self::new(Limits::default())
    }
}

impl GluonSourceLockCodec {
    pub fn new(limits: Limits) -> Self {
        Self {
            engine: GluonEngine::new(limits),
        }
    }
}

impl DeclarationEvaluator<SourceLock> for GluonSourceLockCodec {
    type Identity = EvaluationIdentity;
    type Error = ValidationError;

    fn language_spec(&self) -> &LanguageSpec {
        self.engine.language_spec()
    }

    fn limits(&self) -> Limits {
        self.engine.limits()
    }

    fn with_source_root(&self, source_root: SourceRoot) -> Self {
        Self {
            engine: self.engine.clone().with_source_root(source_root),
        }
    }

    fn evaluate(
        &self,
        source: &Source,
    ) -> Result<
        DeclarationEvaluation<SourceLock, Self::Identity>,
        DeclarationEvaluationError<Self::Error>,
    > {
        let evaluation = self
            .engine
            .evaluate::<GluonSourceLock>(source)
            .map_err(DeclarationEvaluationError::Evaluation)?;
        let lock = SourceLock::try_from(evaluation.value)
            .map_err(DeclarationEvaluationError::Conversion)?;
        lock.validate()
            .map_err(DeclarationEvaluationError::Conversion)?;
        Ok(DeclarationEvaluation {
            value: lock,
            identity: evaluation.identity,
        })
    }
}

impl DeclarationCodec<SourceLock> for GluonSourceLockCodec {
    fn encode(&self, lock: &SourceLock) -> Result<String, Self::Error> {
        Ok(encode_source_lock(lock))
    }
}

/// Encode one canonical, standalone, import-free Gluon lock value.
fn encode_source_lock(lock: &SourceLock) -> String {
    let mut sources = lock.sources.iter().collect::<Vec<_>>();
    sources.sort_by(|left, right| canonical_source_cmp(left, right));

    let mut output = String::from(GENERATED_GLUON_MARKER);
    output.push_str("// Canonical standalone source lock. Schema version is explicit below.\n");
    output.push_str(STANDALONE_GLUON_TYPES);
    output.push_str("{\n");
    writeln!(output, "    schema_version = {},", lock.schema_version).unwrap();
    output.push_str("    sources = [\n");
    for source in sources {
        match source {
            SourceResolution::Archive(source) => encode_archive(&mut output, source),
            SourceResolution::Git(source) => encode_git(&mut output, source),
        }
    }
    output.push_str("    ],\n");
    output.push_str("}\n");
    output
}

fn canonical_source_cmp(left: &SourceResolution, right: &SourceResolution) -> Ordering {
    left.order()
        .cmp(&right.order())
        .then_with(|| source_kind_order(left).cmp(&source_kind_order(right)))
        .then_with(|| match (left, right) {
            (SourceResolution::Archive(left), SourceResolution::Archive(right)) => {
                left.url.cmp(&right.url).then_with(|| left.sha256.cmp(&right.sha256))
            }
            (SourceResolution::Git(left), SourceResolution::Git(right)) => left
                .url
                .cmp(&right.url)
                .then_with(|| left.requested_ref.cmp(&right.requested_ref))
                .then_with(|| left.commit.cmp(&right.commit))
                .then_with(|| left.materialization_sha256.cmp(&right.materialization_sha256)),
            _ => Ordering::Equal,
        })
}

fn source_kind_order(source: &SourceResolution) -> u8 {
    match source {
        SourceResolution::Archive(_) => 0,
        SourceResolution::Git(_) => 1,
    }
}

fn encode_archive(output: &mut String, source: &ArchiveResolution) {
    output.push_str("        Archive {\n");
    writeln!(output, "            order = {},", source.order).unwrap();
    writeln!(output, "            url = {},", gluon_string(&source.url)).unwrap();
    writeln!(output, "            sha256 = {},", gluon_string(&source.sha256)).unwrap();
    output.push_str("        },\n");
}

fn encode_git(output: &mut String, source: &GitResolution) {
    output.push_str("        Git {\n");
    writeln!(output, "            order = {},", source.order).unwrap();
    writeln!(output, "            url = {},", gluon_string(&source.url)).unwrap();
    writeln!(
        output,
        "            requested_ref = {},",
        gluon_string(&source.requested_ref)
    )
    .unwrap();
    writeln!(output, "            commit = {},", gluon_string(&source.commit)).unwrap();
    writeln!(
        output,
        "            materialization_sha256 = {},",
        gluon_string(&source.materialization_sha256)
    )
    .unwrap();
    output.push_str("        },\n");
}

fn gluon_string(value: &str) -> String {
    let mut escaped = String::with_capacity(value.len() + 2);
    escaped.push('"');
    for character in value.chars() {
        match character {
            '"' => escaped.push_str("\\\""),
            '\\' => escaped.push_str("\\\\"),
            '\n' => escaped.push_str("\\n"),
            '\r' => escaped.push_str("\\r"),
            '\t' => escaped.push_str("\\t"),
            character => escaped.push(character),
        }
    }
    escaped.push('"');
    escaped
}
