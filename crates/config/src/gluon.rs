use fs_err as fs;
use gluon_config::{Evaluation, EvaluationFingerprint, Evaluator, Source, SourceRoot};

use super::{Config, Entry, Manager, Resolve};

pub const GENERATED_GLUON_MARKER: &str = "// @generated by os-tools. DO NOT EDIT.\n";

pub type GluonConversionError = Box<dyn Error + Send + Sync + 'static>;

/// Result of evaluating and semantically converting a Gluon fragment.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct DecodedGluon<T> {
    pub value: T,
    pub fingerprint: EvaluationFingerprint,
}

impl<T> From<Evaluation<T>> for DecodedGluon<T> {
    fn from(evaluation: Evaluation<T>) -> Self {
        Self {
            value: evaluation.value,
            fingerprint: evaluation.fingerprint,
        }
    }
}

/// Consumer-owned Gluon/Rust boundary for one configuration domain.
///
/// Implementations evaluate their stable Gluon DTO through the supplied
/// restricted evaluator, convert it into the domain type, and emit standalone
/// canonical literals for generated fragments.
pub trait GluonCodec {
    type Config: Config;

    fn decode(&self, evaluator: &Evaluator, source: &Source) -> Result<DecodedGluon<Self::Config>, GluonCodecError>;

    /// Return a standalone canonical Gluon expression without a generated
    /// marker. [`Manager::save_gluon`] adds the marker atomically.
    fn encode(&self, config: &Self::Config) -> Result<String, GluonCodecError>;
}

#[derive(Debug)]
pub enum GluonCodecError {
    Evaluation(gluon_config::Diagnostic),
    Conversion(GluonConversionError),
}

impl GluonCodecError {
    pub fn conversion(error: impl Error + Send + Sync + 'static) -> Self {
        Self::Conversion(Box::new(error))
    }
}

impl From<gluon_config::Diagnostic> for GluonCodecError {
    fn from(error: gluon_config::Diagnostic) -> Self {
        Self::Evaluation(error)
    }
}

impl fmt::Display for GluonCodecError {
    fn fmt(&self, formatter: &mut fmt::Formatter<'_>) -> fmt::Result {
        match self {
            Self::Evaluation(error) => write!(formatter, "evaluate Gluon value: {error}"),
            Self::Conversion(error) => write!(formatter, "convert Gluon value: {error}"),
        }
    }
}

impl Error for GluonCodecError {
    fn source(&self) -> Option<&(dyn Error + 'static)> {
        match self {
            Self::Evaluation(error) => Some(error),
            Self::Conversion(error) => Some(error.as_ref()),
        }
    }
}

#[derive(Debug, Clone, PartialEq, Eq)]
pub struct LoadedGluonConfig<T> {
    pub logical_name: String,
    pub path: PathBuf,
    pub value: T,
    pub fingerprint: EvaluationFingerprint,
}

#[derive(Debug)]
pub enum LoadGluonError {
    Enumerate {
        path: PathBuf,
        source: io::Error,
    },
    Read {
        path: PathBuf,
        source: io::Error,
    },
    Evaluation {
        path: PathBuf,
        source: gluon_config::Diagnostic,
    },
    Conversion {
        path: PathBuf,
        source: GluonConversionError,
    },
}

impl fmt::Display for LoadGluonError {
    fn fmt(&self, formatter: &mut fmt::Formatter<'_>) -> fmt::Result {
        match self {
            Self::Enumerate { path, .. } => write!(formatter, "enumerate Gluon fragments in {}", path.display()),
            Self::Read { path, .. } => write!(formatter, "read Gluon fragment {}", path.display()),
            Self::Evaluation { path, source } => {
                write!(formatter, "evaluate Gluon fragment {}: {source}", path.display())
            }
            Self::Conversion { path, source } => {
                write!(formatter, "convert Gluon fragment {}: {source}", path.display())
            }
        }
    }
}

impl Error for LoadGluonError {
    fn source(&self) -> Option<&(dyn Error + 'static)> {
        match self {
            Self::Enumerate { source, .. } | Self::Read { source, .. } => Some(source),
            Self::Evaluation { source, .. } => Some(source),
            Self::Conversion { source, .. } => Some(source.as_ref()),
        }
    }
}

#[derive(Debug)]
pub enum SaveGluonError {
    CreateDir {
        path: PathBuf,
        source: io::Error,
    },
    Encode {
        source: GluonCodecError,
    },
    CreateTemporary {
        path: PathBuf,
        source: io::Error,
    },
    WriteTemporary {
        path: PathBuf,
        source: io::Error,
    },
    SyncTemporary {
        path: PathBuf,
        source: io::Error,
    },
    Rename {
        from: PathBuf,
        to: PathBuf,
        source: io::Error,
    },
}

impl fmt::Display for SaveGluonError {
    fn fmt(&self, formatter: &mut fmt::Formatter<'_>) -> fmt::Result {
        match self {
            Self::CreateDir { path, .. } => write!(formatter, "create Gluon config directory {}", path.display()),
            Self::Encode { .. } => formatter.write_str("encode generated Gluon fragment"),
            Self::CreateTemporary { path, .. } => {
                write!(formatter, "create temporary Gluon fragment {}", path.display())
            }
            Self::WriteTemporary { path, .. } => {
                write!(formatter, "write temporary Gluon fragment {}", path.display())
            }
            Self::SyncTemporary { path, .. } => {
                write!(formatter, "sync temporary Gluon fragment {}", path.display())
            }
            Self::Rename { from, to, .. } => {
                write!(formatter, "rename {} to {}", from.display(), to.display())
            }
        }
    }
}

impl Error for SaveGluonError {
    fn source(&self) -> Option<&(dyn Error + 'static)> {
        match self {
            Self::CreateDir { source, .. }
            | Self::CreateTemporary { source, .. }
            | Self::WriteTemporary { source, .. }
            | Self::SyncTemporary { source, .. }
            | Self::Rename { source, .. } => Some(source),
            Self::Encode { source } => Some(source),
        }
    }
}

impl Manager {
    /// Load only `.glu` fragments using explicit vendor, admin, and user
    /// precedence. Higher-priority fragments replace lower-priority fragments
    /// with the same logical name.
    pub fn load_gluon<C: GluonCodec>(
        &self,
        evaluator: &Evaluator,
        codec: &C,
    ) -> Result<Vec<LoadedGluonConfig<C::Config>>, LoadGluonError> {
        let domain = C::Config::domain();
        let mut loaded = BTreeMap::new();

        for (entry, resolve) in self.scope.load_with() {
            for fragment in enumerate_gluon_paths(entry, resolve, &domain)? {
                let content = fs::read_to_string(&fragment.path).map_err(|source| LoadGluonError::Read {
                    path: fragment.path.clone(),
                    source,
                })?;
                let source = Source::new(fragment.source_name, content);
                let source_root =
                    SourceRoot::new(&fragment.source_root).map_err(|source| LoadGluonError::Evaluation {
                        path: fragment.path.clone(),
                        source,
                    })?;
                let evaluator = evaluator.clone().with_source_root(source_root);
                let decoded = codec.decode(&evaluator, &source).map_err(|error| match error {
                    GluonCodecError::Evaluation(source) => LoadGluonError::Evaluation {
                        path: fragment.path.clone(),
                        source,
                    },
                    GluonCodecError::Conversion(source) => LoadGluonError::Conversion {
                        path: fragment.path.clone(),
                        source,
                    },
                })?;

                loaded.insert(
                    fragment.logical_name.clone(),
                    LoadedGluonConfig {
                        logical_name: fragment.logical_name,
                        path: fragment.path,
                        value: decoded.value,
                        fingerprint: decoded.fingerprint,
                    },
                );
            }
        }

        Ok(loaded.into_values().collect())
    }

    /// Atomically save one generated `.glu` fragment.
    pub fn save_gluon<C: GluonCodec>(
        &self,
        name: impl fmt::Display,
        config: &C::Config,
        codec: &C,
    ) -> Result<PathBuf, SaveGluonError> {
        let domain = C::Config::domain();
        let dir = self.scope.save_dir(&domain);
        fs::create_dir_all(&dir).map_err(|source| SaveGluonError::CreateDir {
            path: dir.clone(),
            source,
        })?;

        let path = dir.join(format!("{name}.glu"));
        let literal = codec
            .encode(config)
            .map_err(|source| SaveGluonError::Encode { source })?;
        let mut generated = String::with_capacity(GENERATED_GLUON_MARKER.len() + literal.len() + 1);
        generated.push_str(GENERATED_GLUON_MARKER);
        generated.push_str(&literal);
        if !generated.ends_with('\n') {
            generated.push('\n');
        }
        atomic_write(&path, generated.as_bytes())?;
        Ok(path)
    }

    /// Delete only the generated Gluon fragment, leaving legacy formats
    /// untouched during the migration window.
    pub fn delete_gluon<T: Config>(&self, name: impl fmt::Display) -> io::Result<()> {
        let path = self.scope.save_dir(&T::domain()).join(format!("{name}.glu"));
        match fs::remove_file(path) {
            Ok(()) => Ok(()),
            Err(error) if error.kind() == io::ErrorKind::NotFound => Ok(()),
            Err(error) => Err(error),
        }
    }
}

#[derive(Debug)]
struct GluonPath {
    logical_name: String,
    source_name: String,
    source_root: PathBuf,
    path: PathBuf,
}

fn enumerate_gluon_paths(entry: Entry, resolve: Resolve<'_>, domain: &str) -> Result<Vec<GluonPath>, LoadGluonError> {
    let source_root = resolve.config_dir();
    match entry {
        Entry::File => {
            let path = resolve.file(domain, "glu");
            if path.is_file() {
                Ok(vec![GluonPath {
                    logical_name: domain.to_owned(),
                    source_name: format!("{domain}.glu"),
                    source_root,
                    path,
                }])
            } else {
                Ok(Vec::new())
            }
        }
        Entry::Directory => {
            let dir = resolve.dir(domain);
            let entries = match fs::read_dir(&dir) {
                Ok(entries) => entries,
                Err(error) if error.kind() == io::ErrorKind::NotFound => return Ok(Vec::new()),
                Err(source) => return Err(LoadGluonError::Enumerate { path: dir, source }),
            };
            let mut paths = Vec::new();
            for entry in entries {
                let entry = entry.map_err(|source| LoadGluonError::Enumerate {
                    path: dir.clone(),
                    source,
                })?;
                let path = entry.path();
                if !path.is_file() || path.extension().and_then(|extension| extension.to_str()) != Some("glu") {
                    continue;
                }
                let logical_name = path
                    .file_stem()
                    .and_then(|name| name.to_str())
                    .ok_or_else(|| LoadGluonError::Enumerate {
                        path: dir.clone(),
                        source: io::Error::new(io::ErrorKind::InvalidData, "Gluon fragment name is not UTF-8"),
                    })?
                    .to_owned();
                paths.push(GluonPath {
                    source_name: format!("{domain}.d/{logical_name}.glu"),
                    logical_name,
                    source_root: source_root.clone(),
                    path,
                });
            }
            paths.sort_by(|left, right| left.logical_name.cmp(&right.logical_name));
            Ok(paths)
        }
    }
}

static TEMPORARY_COUNTER: AtomicU64 = AtomicU64::new(0);

fn atomic_write(path: &Path, bytes: &[u8]) -> Result<(), SaveGluonError> {
    let parent = path
        .parent()
        .expect("generated Gluon fragment always has a parent directory");
    let file_name = path
        .file_name()
        .expect("generated Gluon fragment always has a file name")
        .to_string_lossy();
    let mut temporary = None;
    for _ in 0..100 {
        let counter = TEMPORARY_COUNTER.fetch_add(1, Ordering::Relaxed);
        let candidate = parent.join(format!(".{file_name}.tmp-{}-{counter}", std::process::id()));
        match fs::OpenOptions::new().write(true).create_new(true).open(&candidate) {
            Ok(file) => {
                temporary = Some((candidate, file));
                break;
            }
            Err(error) if error.kind() == io::ErrorKind::AlreadyExists => {}
            Err(source) => {
                return Err(SaveGluonError::CreateTemporary {
                    path: candidate,
                    source,
                });
            }
        }
    }
    let (temporary_path, mut file) = temporary.ok_or_else(|| SaveGluonError::CreateTemporary {
        path: parent.to_owned(),
        source: io::Error::new(
            io::ErrorKind::AlreadyExists,
            "unable to allocate a temporary fragment name",
        ),
    })?;
    let mut guard = TemporaryGuard(Some(temporary_path.clone()));
    file.write_all(bytes).map_err(|source| SaveGluonError::WriteTemporary {
        path: temporary_path.clone(),
        source,
    })?;
    file.sync_all().map_err(|source| SaveGluonError::SyncTemporary {
        path: temporary_path.clone(),
        source,
    })?;
    drop(file);
    fs::rename(&temporary_path, path).map_err(|source| SaveGluonError::Rename {
        from: temporary_path,
        to: path.to_owned(),
        source,
    })?;
    guard.0 = None;
    Ok(())
}

struct TemporaryGuard(Option<PathBuf>);

impl Drop for TemporaryGuard {
    fn drop(&mut self) {
        if let Some(path) = self.0.take() {
            let _ = fs::remove_file(path);
        }
    }
}

#[cfg(test)]
mod tests {
    use super::*;
    use crate::Scope;

    impl Config for String {
        fn domain() -> String {
            "dummy".to_owned()
        }
    }

    struct StringCodec;

    impl GluonCodec for StringCodec {
        type Config = String;

        fn decode(
            &self,
            evaluator: &Evaluator,
            source: &Source,
        ) -> Result<DecodedGluon<Self::Config>, GluonCodecError> {
            let evaluation = evaluator.evaluate::<String>(source)?;
            if evaluation.value == "reject" {
                return Err(GluonCodecError::conversion(io::Error::new(
                    io::ErrorKind::InvalidData,
                    "rejected dummy value",
                )));
            }
            Ok(evaluation.into())
        }

        fn encode(&self, config: &Self::Config) -> Result<String, GluonCodecError> {
            Ok(format!("\"{config}\""))
        }
    }

    fn write(path: impl AsRef<Path>, content: &str) {
        let path = path.as_ref();
        fs::create_dir_all(path.parent().unwrap()).unwrap();
        fs::write(path, content).unwrap();
    }

    fn layered_manager(root: &Path, user: &Path, program: &str) -> Manager {
        Manager {
            scope: Scope::User {
                root: root.to_owned(),
                config: user.to_owned(),
                program: program.to_owned(),
            },
        }
    }

    #[test]
    fn gluon_fragments_have_deterministic_order_and_explicit_precedence() {
        let temporary = tempfile::tempdir().unwrap();
        let root = temporary.path().join("root");
        let user = temporary.path().join("user");
        let program = "config-gluon-precedence";
        let vendor = root.join("usr/share").join(program);
        let admin = root.join("etc").join(program);
        let user_program = user.join(program);

        write(vendor.join("dummy.glu"), "\"vendor-base\"");
        write(vendor.join("dummy.d/z.glu"), "\"vendor-z\"");
        write(vendor.join("dummy.d/a.glu"), "\"vendor-a\"");
        write(vendor.join("dummy.d/shared.glu"), "\"vendor-shared\"");
        write(admin.join("dummy.glu"), "\"admin-base\"");
        write(admin.join("dummy.d/shared.glu"), "\"admin-shared\"");
        write(user_program.join("dummy.glu"), "\"user-base\"");
        write(user_program.join("dummy.d/shared.glu"), "\"user-shared\"");

        let loaded = layered_manager(&root, &user, program)
            .load_gluon(&Evaluator::default(), &StringCodec)
            .unwrap();
        assert_eq!(
            loaded
                .iter()
                .map(|fragment| (fragment.logical_name.as_str(), fragment.value.as_str()))
                .collect::<Vec<_>>(),
            [
                ("a", "vendor-a"),
                ("dummy", "user-base"),
                ("shared", "user-shared"),
                ("z", "vendor-z"),
            ]
        );
    }

    #[test]
    fn malformed_and_conversion_errors_are_visible_with_their_paths() {
        let temporary = tempfile::tempdir().unwrap();
        let manager = Manager::custom(temporary.path());
        let malformed = temporary.path().join("dummy.d/malformed.glu");
        write(&malformed, "let value = in value");

        let error = manager.load_gluon(&Evaluator::default(), &StringCodec).unwrap_err();
        let LoadGluonError::Evaluation { path, source } = error else {
            panic!("expected evaluation error");
        };
        assert_eq!(path, malformed);
        assert_eq!(source.source_name.as_deref(), Some("dummy.d/malformed.glu"));
        assert!(source.span.is_some());

        fs::remove_file(&path).unwrap();
        let rejected = temporary.path().join("dummy.d/rejected.glu");
        write(&rejected, "\"reject\"");
        let error = manager.load_gluon(&Evaluator::default(), &StringCodec).unwrap_err();
        let LoadGluonError::Conversion { path, source } = error else {
            panic!("expected conversion error");
        };
        assert_eq!(path, rejected);
        assert!(source.to_string().contains("rejected dummy value"));
    }

    #[test]
    fn load_gluon_never_falls_back_to_yaml_or_kdl() {
        let temporary = tempfile::tempdir().unwrap();
        let manager = Manager::custom(temporary.path());
        write(temporary.path().join("dummy.yaml"), "not: valid: yaml");
        write(temporary.path().join("dummy.kdl"), "not valid kdl {");
        write(temporary.path().join("dummy.d/fragment.yaml"), "\"yaml\"");
        write(temporary.path().join("dummy.d/fragment.kdl"), "\"kdl\"");

        let loaded = manager.load_gluon(&Evaluator::default(), &StringCodec).unwrap();
        assert!(loaded.is_empty());

        write(temporary.path().join("dummy.d/fragment.glu"), "\"gluon\"");
        let loaded = manager.load_gluon(&Evaluator::default(), &StringCodec).unwrap();
        assert_eq!(loaded.len(), 1);
        assert_eq!(loaded[0].value, "gluon");
    }

    #[test]
    fn fragment_relative_imports_are_contained_to_the_layer_root() {
        let temporary = tempfile::tempdir().unwrap();
        let manager = Manager::custom(temporary.path());
        write(temporary.path().join("dummy.d/modules/value.glu"), "\"imported\"");
        write(
            temporary.path().join("dummy.d/fragment.glu"),
            "import! \"modules/value.glu\"",
        );

        let loaded = manager.load_gluon(&Evaluator::default(), &StringCodec).unwrap();

        assert_eq!(loaded.len(), 1);
        assert_eq!(loaded[0].value, "imported");
        assert_eq!(
            loaded[0]
                .fingerprint
                .imported_modules
                .iter()
                .map(|module| module.logical_name.as_str())
                .collect::<Vec<_>>(),
            ["dummy.d/modules/value.glu"]
        );
    }

    #[test]
    fn save_and_delete_gluon_use_a_generated_atomic_fragment() {
        let temporary = tempfile::tempdir().unwrap();
        let manager = Manager::custom(temporary.path());
        let value = "saved".to_owned();
        let path = manager.save_gluon("example", &value, &StringCodec).unwrap();

        let content = fs::read_to_string(&path).unwrap();
        assert_eq!(content, format!("{GENERATED_GLUON_MARKER}\"saved\"\n"));
        manager
            .save_gluon("example", &"updated".to_owned(), &StringCodec)
            .unwrap();
        assert_eq!(
            fs::read_to_string(&path).unwrap(),
            format!("{GENERATED_GLUON_MARKER}\"updated\"\n")
        );
        assert_eq!(
            manager.load_gluon(&Evaluator::default(), &StringCodec).unwrap()[0].value,
            "updated"
        );
        assert_eq!(
            fs::read_dir(path.parent().unwrap())
                .unwrap()
                .filter_map(Result::ok)
                .filter(|entry| entry.file_name().to_string_lossy().contains(".tmp-"))
                .count(),
            0
        );

        write(path.with_extension("yaml"), "legacy: true");
        write(path.with_extension("kdl"), "legacy #true");
        manager.delete_gluon::<String>("example").unwrap();
        assert!(!path.exists());
        assert!(path.with_extension("yaml").exists());
        assert!(path.with_extension("kdl").exists());
        manager.delete_gluon::<String>("example").unwrap();
    }
}
