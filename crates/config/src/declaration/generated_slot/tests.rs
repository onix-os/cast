use std::{ffi::OsString, io, path::Path};

use declarative_config::{EngineId, LanguageId, LanguageSpec};
use fs_err as fs;

use super::*;

const ALPHA_MARKER: &[u8] = b"// alpha domain owner\n";
const BETA_MARKER: &[u8] = b"-- beta domain owner\n";

fn language(name: &str, extension: &str) -> LanguageSpec {
    LanguageSpec::new(
        LanguageId::new(name).unwrap(),
        EngineId::new(format!("{name}-engine"), "1").unwrap(),
        extension,
        "fixture-v1",
        format!("# generated by {name}\n"),
    )
    .unwrap()
}

fn authority(
    name: &str,
    extension: &str,
    marker: &[u8],
) -> GeneratedDeclarationAuthority {
    GeneratedDeclarationAuthority::new(
        language(name, extension),
        marker,
    )
    .unwrap()
}

fn slot(
    directory: &Path,
    name: &str,
    marker: &str,
) -> GeneratedDeclarationSlot {
    single_authority_slot(
        directory,
        name,
        marker,
        1024,
        ".fixture-tmp-",
    )
    .unwrap()
}

fn single_authority_slot(
    directory: &Path,
    name: &str,
    marker: impl Into<Vec<u8>>,
    size_limit: usize,
    temporary_prefix: &str,
) -> Result<GeneratedDeclarationSlot, GeneratedDeclarationSlotError> {
    let active = GeneratedDeclarationAuthority::new(
        language("fixture", "decl"),
        marker,
    )?;
    GeneratedDeclarationSlot::with_registered_authorities(
        directory,
        name,
        [active.clone()],
        active,
        size_limit,
        temporary_prefix,
    )
}

fn registered_slot(
    directory: &Path,
    active_extension: &str,
) -> GeneratedDeclarationSlot {
    let alpha = authority("alpha", "alpha", ALPHA_MARKER);
    let beta = authority("beta", "beta", BETA_MARKER);
    let active = match active_extension {
        "alpha" => alpha.clone(),
        "beta" => beta.clone(),
        extension => panic!("unknown fixture extension {extension}"),
    };
    GeneratedDeclarationSlot::with_registered_authorities(
        directory,
        "system",
        [alpha, beta],
        active,
        1024,
        ".fixture-tmp-",
    )
    .unwrap()
}

fn temporary_names(directory: &Path) -> Vec<OsString> {
    let mut names = fs::read_dir(directory)
        .unwrap()
        .filter_map(Result::ok)
        .map(|entry| entry.file_name())
        .filter(|name| name.to_string_lossy().starts_with(".fixture-tmp-"))
        .collect::<Vec<_>>();
    names.sort();
    names
}

fn public_count(directory: &Path) -> usize {
    ["system.alpha", "system.beta"]
        .into_iter()
        .filter(|name| directory.join(name).exists())
        .count()
}

#[test]
fn slot_preserves_exact_bytes_and_uses_explicit_language_extension() {
    let temporary = tempfile::tempdir().unwrap();
    let slot = slot(temporary.path(), "system", "# owned by system slot\n");
    let bytes = b"# owned by system slot\nvalue-without-final-newline";

    let path = slot.save(bytes).unwrap();

    assert_eq!(path, temporary.path().join("system.decl"));
    assert_eq!(fs::read(&path).unwrap(), bytes);
    assert_eq!(slot.language_spec().extension(), "decl");
    assert_eq!(slot.ownership_marker(), b"# owned by system slot\n");

    slot.delete().unwrap();
    assert!(!path.exists());
}

#[test]
fn ownership_marker_is_slot_specific_not_a_language_detector() {
    let temporary = tempfile::tempdir().unwrap();
    let first = slot(temporary.path(), "snapshot", "# snapshot owner\n");
    let competing = slot(temporary.path(), "snapshot", "# other owner\n");
    first.save(b"# snapshot owner\nvalue").unwrap();

    let error = competing.save(b"# other owner\nreplacement").unwrap_err();

    assert!(matches!(
        error,
        SaveDeclarationError::AuthoredDeclaration { .. }
    ));
    assert_eq!(
        fs::read(first.path()).unwrap(),
        b"# snapshot owner\nvalue"
    );
}

#[test]
fn authored_files_and_unmarked_generated_bytes_are_rejected() {
    let temporary = tempfile::tempdir().unwrap();
    let slot = slot(temporary.path(), "profile", "# generated profile\n");
    fs::write(slot.path(), "authored value\n").unwrap();

    assert!(matches!(
        slot.save(b"# generated profile\nreplacement"),
        Err(SaveDeclarationError::AuthoredDeclaration { .. })
    ));
    assert!(matches!(
        slot.delete(),
        Err(DeleteDeclarationError::AuthoredDeclaration { .. })
    ));

    fs::remove_file(slot.path()).unwrap();
    assert!(matches!(
        slot.save(b"missing marker"),
        Err(SaveDeclarationError::MissingOwnershipMarker { .. })
    ));
}

#[test]
fn absent_target_race_does_not_replace_new_authored_content() {
    let temporary = tempfile::tempdir().unwrap();
    let slot = slot(temporary.path(), "race", "# generated race\n");
    let path = slot.path();

    let error = slot
        .save_with_hook(b"# generated race\nmanaged", |_| {
            fs::write(&path, "authored during save\n").unwrap();
        })
        .unwrap_err();

    assert!(matches!(error, SaveDeclarationError::Rename { .. }));
    assert_eq!(fs::read_to_string(path).unwrap(), "authored during save\n");
    assert!(temporary_names(temporary.path()).is_empty());
}

#[test]
fn replacement_race_revalidates_the_generated_target() {
    let temporary = tempfile::tempdir().unwrap();
    let slot = slot(temporary.path(), "race", "# generated race\n");
    let path = slot.save(b"# generated race\nold").unwrap();

    let error = slot
        .save_with_hook(b"# generated race\nnew", |_| {
            fs::remove_file(&path).unwrap();
            fs::write(&path, "authored replacement\n").unwrap();
        })
        .unwrap_err();

    assert!(matches!(error, SaveDeclarationError::ReadExisting { .. }));
    assert_eq!(fs::read_to_string(path).unwrap(), "authored replacement\n");
    assert!(temporary_names(temporary.path()).is_empty());
}

#[test]
fn registered_authority_switch_never_exposes_two_public_outputs() {
    let temporary = tempfile::tempdir().unwrap();
    let slot = registered_slot(temporary.path(), "alpha");
    let alpha = temporary.path().join("system.alpha");
    let beta = temporary.path().join("system.beta");
    fs::write(&beta, [BETA_MARKER, b"old"].concat()).unwrap();
    let mut observed_counts = Vec::new();

    let saved = slot
        .save_with_phase_hook(
            &[ALPHA_MARKER, b"new"].concat(),
            |_| {
                observed_counts.push(public_count(temporary.path()));
                Ok(())
            },
        )
        .unwrap();

    assert_eq!(saved, alpha);
    assert_eq!(observed_counts, [1, 0, 1]);
    assert_eq!(public_count(temporary.path()), 1);
    assert_eq!(fs::read(&alpha).unwrap(), [ALPHA_MARKER, b"new"].concat());
    assert!(!beta.exists());
    assert!(!slot.switch_residue_path().exists());
    assert!(temporary_names(temporary.path()).is_empty());
}

#[test]
fn retired_alternate_residue_is_resumed_after_interruption() {
    let temporary = tempfile::tempdir().unwrap();
    let slot = registered_slot(temporary.path(), "alpha");
    let alpha = temporary.path().join("system.alpha");
    let beta = temporary.path().join("system.beta");
    fs::write(&beta, [BETA_MARKER, b"old"].concat()).unwrap();

    let error = slot
        .save_with_phase_hook(
            &[ALPHA_MARKER, b"first"].concat(),
            |phase| {
                if phase == AuthoritySwitchPhase::PreviousRetired {
                    Err(io::Error::new(io::ErrorKind::Interrupted, "fixture"))
                } else {
                    Ok(())
                }
            },
        )
        .unwrap_err();

    assert!(matches!(error, SaveDeclarationError::ReadExisting { .. }));
    assert_eq!(public_count(temporary.path()), 0);
    assert!(!alpha.exists());
    assert!(!beta.exists());
    assert!(slot.switch_residue_path().exists());
    assert!(temporary_names(temporary.path()).is_empty());

    drop(slot);
    let slot = registered_slot(temporary.path(), "alpha");
    slot.save(&[ALPHA_MARKER, b"retry"].concat()).unwrap();
    assert_eq!(fs::read(alpha).unwrap(), [ALPHA_MARKER, b"retry"].concat());
    assert_eq!(public_count(temporary.path()), 1);
    assert!(!slot.switch_residue_path().exists());
}

