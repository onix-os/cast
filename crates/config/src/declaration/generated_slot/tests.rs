#[test]
fn registered_slot_policy_validates_the_complete_authority_set() {
    let directory = tempfile::tempdir().unwrap();
    let alpha = authority("alpha", "alpha", ALPHA_MARKER);
    let beta = authority("beta", "beta", BETA_MARKER);

    assert!(matches!(
        GeneratedDeclarationSlot::with_registered_authorities(
            directory.path(),
            "system",
            std::iter::empty(),
            alpha.clone(),
            1024,
            ".fixture-tmp-",
        ),
        Err(GeneratedDeclarationSlotError::NoRegisteredAuthorities)
    ));
    assert!(matches!(
        GeneratedDeclarationSlot::with_registered_authorities(
            directory.path(),
            "system",
            [alpha.clone(), alpha.clone()],
            alpha.clone(),
            1024,
            ".fixture-tmp-",
        ),
        Err(GeneratedDeclarationSlotError::DuplicateAuthorityExtension { .. })
    ));
    assert!(matches!(
        GeneratedDeclarationSlot::with_registered_authorities(
            directory.path(),
            "system",
            [alpha.clone()],
            beta,
            1024,
            ".fixture-tmp-",
        ),
        Err(GeneratedDeclarationSlotError::ActiveAuthorityNotRegistered { .. })
    ));
    let mismatched = GeneratedDeclarationAuthority::new(
        alpha.language_spec().clone(),
        b"different owner\n",
    )
    .unwrap();
    assert!(matches!(
        GeneratedDeclarationSlot::with_registered_authorities(
            directory.path(),
            "system",
            [alpha],
            mismatched,
            1024,
            ".fixture-tmp-",
        ),
        Err(GeneratedDeclarationSlotError::ActiveAuthorityMismatch { .. })
    ));
}

#[test]
fn registered_single_authority_rejects_unsafe_policy() {
    let directory = tempfile::tempdir().unwrap();
    for name in ["", ".", "..", "nested/name", "nested\\name"] {
        assert!(matches!(
            single_authority_slot(
                directory.path(),
                name,
                "# marker\n",
                1,
                ".tmp-",
            ),
            Err(GeneratedDeclarationSlotError::InvalidName { .. })
        ));
    }
    assert!(matches!(
        single_authority_slot(
            directory.path(),
            "safe",
            Vec::new(),
            1,
            ".tmp-",
        ),
        Err(GeneratedDeclarationSlotError::InvalidOwnershipMarker)
    ));
    assert!(matches!(
        single_authority_slot(
            directory.path(),
            "safe",
            "# marker\n",
            0,
            ".tmp-",
        ),
        Err(GeneratedDeclarationSlotError::ZeroSizeLimit)
    ));
    assert!(matches!(
        single_authority_slot(
            directory.path(),
            "safe",
            "# marker\n",
            32,
            "../tmp-",
        ),
        Err(GeneratedDeclarationSlotError::InvalidTemporaryPrefix { .. })
    ));
}
