#[test]
fn collisions_and_authored_alternates_fail_closed() {
    let collision_directory = tempfile::tempdir().unwrap();
    let collision_slot = registered_slot(collision_directory.path(), "alpha");
    let alpha = collision_directory.path().join("system.alpha");
    let beta = collision_directory.path().join("system.beta");
    fs::write(&alpha, [ALPHA_MARKER, b"first"].concat()).unwrap();
    fs::write(&beta, [BETA_MARKER, b"second"].concat()).unwrap();

    assert!(matches!(
        collision_slot.save(&[ALPHA_MARKER, b"new"].concat()),
        Err(SaveDeclarationError::ReadExisting { .. })
    ));
    assert!(matches!(
        collision_slot.delete(),
        Err(DeleteDeclarationError::ReadExisting { .. })
    ));
    assert!(alpha.exists() && beta.exists());

    let authored_directory = tempfile::tempdir().unwrap();
    let authored_slot = registered_slot(authored_directory.path(), "alpha");
    let authored = authored_directory.path().join("system.beta");
    fs::write(&authored, b"authored beta declaration\n").unwrap();
    assert!(matches!(
        authored_slot.save(&[ALPHA_MARKER, b"new"].concat()),
        Err(SaveDeclarationError::AuthoredDeclaration { .. })
    ));
    assert!(matches!(
        authored_slot.delete(),
        Err(DeleteDeclarationError::AuthoredDeclaration { .. })
    ));
    assert_eq!(fs::read(authored).unwrap(), b"authored beta declaration\n");
}

